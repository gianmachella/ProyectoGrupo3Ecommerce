     */
    public function store(Request $request)
    {
      $photoPath = $request['photo']->store('public/ProductsPhotos');

     Product::create([
          'name' => $request['name'],
          'description1' => $request['description1'],
          'description2' => $request['description2'],
          'price' => $request['price'],
          'stock' => $request['stock'],
          'photo' => $photoPath,
      ]);

      return redirect('/productos');
    }

